    public static Object[][] quickSort(Object[][] arreglo, int primero, int ultimo, boolean ascendente) {
        int i = primero;
        int j = ultimo;
        //Obtendremos el pivote que esta en medio entre las 2 posiciones
        double pivote = (double) arreglo[(primero + ultimo) / 2][0];
        //Este ciclo se dejara de ejecutar cuando ambas posiciones (i Y j) se crucen
        do {
            //Estos ciclos determinara desde que posicion se realizara el intercambio
            if (ascendente) {
                while ((double) arreglo[i][0] < pivote) {
                    i++;
                }
                while ((double) arreglo[j][0] > pivote) {
                    j--;
                }
            } else {
                while ((double) arreglo[i][0] > pivote) {
                    i++;
                }
                while ((double) arreglo[j][0] < pivote) {
                    j--;
                }
            }
            //Esta condicion determina si no se han cruzado las posiciones
            //asi para poder realizar el intercambio
            if (i <= j) {
                double temporal = (double) arreglo[j][0];
                Dulce temporaldul = (Dulce) arreglo[j][1];

                arreglo[j][0] = (double) arreglo[i][0];
                arreglo[j][1] = (Dulce) arreglo[i][1];

                arreglo[i][0] = temporal;
                arreglo[i][1] = temporaldul;

                i++;
                j--;
            }
        } while (i <= j);
        //Como el arreglo ya esta separado entre menores y mayores (del pivote)
        //se empezara a ordenar ambas mitades
        if (primero < j) {
            quickSort(arreglo, primero, j, ascendente);
        }
        if (ultimo > i) {
            quickSort(arreglo, i, ultimo, ascendente);
        }
        return arreglo;
    }

